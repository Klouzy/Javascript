// Data Types
let number = 10; // number
console.log('The 1st value is a ' + typeof number);
let string = 'Yo!'; // string (single quotes)
console.log('The 2nd value is a ' + typeof string);
let boolean = true; // true or false
console.log('The 3rd value is a ' + typeof boolean);
let undefined; // Undefined type
console.log('The 4th value is ' + typeof undefined);