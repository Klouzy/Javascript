// Expressions
// 1. Variable declaration
let x; // so here we have operator "let" and operand "x"

// 2. Assing value
x = 100;

// 3. perform an evaluation that returns a single value
let a = 1;
let b = 9;
let c = a + b;
console.log(c);

/* So in the example above we did:
1. variables declaration
2. performed an evaluation a + b
3. assigned the result to c */